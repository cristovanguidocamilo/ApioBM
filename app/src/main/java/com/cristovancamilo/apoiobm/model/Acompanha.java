package com.cristovancamilo.apoiobm.model;

public class Acompanha {

    private String total;
    private String abatidos;
    private String restam;

    public Acompanha() {
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getAbatidos() {
        return abatidos;
    }

    public void setAbatidos(String abatidos) {
        this.abatidos = abatidos;
    }

    public String getRestam() {
        return restam;
    }

    public void setRestam(String restam) {
        this.restam = restam;
    }
}
