package com.cristovancamilo.apoiobm.model;

public class EscalaAbate {

    private String lote;
    private String subLote;
    private String quantLote;
    private String currais;
    private String nome;
    private String nomeFazenda;
    private String habilitacao;
    private String statusLote;
    private String brincado;

    public EscalaAbate() {
    }

    public String getLote() {
        return lote;
    }

    public void setLote(String lote) {
        this.lote = lote;
    }

    public String getSubLote() {
        return subLote;
    }

    public void setSubLote(String subLote) {
        this.subLote = subLote;
    }

    public String getQuantLote() {
        return quantLote;
    }

    public void setQuantLote(String quantLote) {
        this.quantLote = quantLote;
    }

    public String getCurrais() {
        return currais;
    }

    public void setCurrais(String currais) {
        this.currais = currais;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNomeFazenda() {
        return nomeFazenda;
    }

    public void setNomeFazenda(String nomeFazenda) {
        this.nomeFazenda = nomeFazenda;
    }

    public String getHabilitacao() {
        return habilitacao;
    }

    public void setHabilitacao(String habilitacao) {
        this.habilitacao = habilitacao;
    }

    public String getStatusLote() {
        return statusLote;
    }

    public void setStatusLote(String statusLote) {
        this.statusLote = statusLote;
    }

    public String getBrincado() { return brincado; }

    public void setBrincado(String brincado) { this.brincado = brincado; }
}
